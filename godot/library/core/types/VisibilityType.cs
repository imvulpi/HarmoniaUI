namespace HarmoniaUI.library.core.types
{
    /// <summary>
    /// Defines elements visibility.
    /// </summary>
    public enum VisibilityType
    {
        /// <summary>
        /// Element is completely visible
        /// </summary>
        Visible,

        /// <summary>
        /// Element occupies space, but is not visible
        /// </summary>
        Transparent,

        /// <summary>
        /// Element doesn't occupy any space and is hidden.
        /// </summary>
        Hidden
    }
}
